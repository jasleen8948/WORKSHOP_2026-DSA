# searching is a process of finding a specific element in a collection of elements.
# There are different searching algorithms, each with its own advantages and disadvantages. 
# Some common searching algorithms include:
# linear search, binary search, jump search, interpolation search, exponential search, Fibonacci search


# | Algorithm            | Best | Average      | Worst    | Sorted Required |
# | -------------------- | ---- | ------------ | -------- | --------------- |
# | Linear Search        | O(1) | O(n)         | O(n)     | No              |
# | Binary Search        | O(1) | O(log n)     | O(log n) | Yes             |
# | Jump Search          | O(1) | O(√n)        | O(√n)    | Yes             |
# | Interpolation Search | O(1) | O(log log n) | O(n)     | Yes             |
# | Exponential Search   | O(1) | O(log n)     | O(log n) | Yes             |
# | Fibonacci Search     | O(1) | O(log n)     | O(log n) | Yes             |